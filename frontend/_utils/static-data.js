export const decodeData = (dataType, idsToDecode) => {
  const allData = staticData[dataType];
  const decoded = [];
  if (allData && allData.length > 0 && idsToDecode) {
    for (const idToDecode of idsToDecode) {
      const decodedItem = allData.find(el => `${el.id}` === `${idToDecode}`);
      if (decodedItem) {
        decoded.push(decodedItem);
      }
    }
  }
  return decoded;
};
